        <h3 class="lead">Presbiopia</h3>

        <h4>Apa Itu Presbiopi (Mata Tua)?</h4>
        <p>Seperti yang telah dijelaskan bahwa presbiopi adalah kondisi ketika seseorang kehilangan kemampuan untuk
            melihat objek dari jarak dekat dengan jelas. Kondisi ini kerap mulai terjadi pada usia 40 tahun dan bisa
            terus memburuk hingga berusia 65 tahun.</p>

        <h5>Penyebab Presbiopi</h5>
        <p>Pada dasarnya, proses melihat dimulai ketika mata menangkap cahaya yang memantul dari suatu objek. Cahaya
            tersebut kemudian akan menembus kornea dan diteruskan ke lensa mata yang terletak di belakang iris.</p>
        <p>Setelah itu, lensa akan mengarahkan cahaya yang masuk ke retina untuk diubah menjadi sinyal listrik sebelum
            dikirimkan ke otak. Karena itu, kejernihan gambar yang diterima oleh otak tergantung pada kemampuan lensa
            saat mengarahkan cahaya ke retina.</p>
        <p>Pada kasus presbiopi, cahaya tidak jatuh tepat di retina sehingga membuat penglihatan menjadi buram. Hal ini
            dapat terjadi karena otot-otot di sekitar lensa mata mulai mengeras dan sudah tidak elastis akibat
            pertambahan usia.</p>
        <p>Di samping itu, sejumlah faktor lainnya yang dapat meningkatkan risiko seseorang mengalami presbiopi adalah
            sebagai berikut.</p>
        <ul>
            <li>Berusia di atas 40 tahun.</li>
            <li>Menderita kondisi medis tertentu, seperti diabetes, penyakit kardiovaskular, atau multiple sclerosis.
            </li>
            <li>Efek samping dari penggunaan obat-obatan tertentu, seperti obat golongan diuretik, antihistamin, atau
                antidepresan.</li>
        </ul>

        <h5>Gejala Presbiopi</h5>
        <p>Presbiopi umumnya berkembang secara bertahap. Karena itu, penderita kondisi ini biasanya baru menyadari
            gejalanya setelah berusia 40 tahun ke atas. Adapun beberapa gejala umum presbiopi adalah:</p>
        <ul>
            <li>Membutuhkan pencahayaan yang lebih terang saat sedang membaca.</li>
            <li>Kebiasaan menyipitkan mata untuk melihat objek jarak dekat dengan jelas.</li>
            <li>Penglihatan kabur saat membaca dengan jarak normal.</li>
            <li>Kesulitan untuk membaca huruf yang berukuran kecil.</li>
            <li>Nyeri kepala atau mata merasa sangat lelah setelah membaca atau melihat objek dalam jarak dekat.</li>
            <li>Sering memegang objek pada jarak yang lebih jauh agar bisa melihat dengan lebih jelas.</li>
        </ul>

        <h5>Komplikasi Presbiopi</h5>
        <p>Presbiopi yang tidak ditangani dengan tepat dapat semakin memburuk dan membuat penderitanya kesulitan untuk
            melakukan pekerjaan serta aktivitas sehari-hari. Selain itu, kondisi ini juga bisa menyebabkan mata harus
            bekerja lebih keras, terutama saat melakukan pekerjaan yang membutuhkan ketelitian tinggi. Jika terjadi
            secara terus-menerus, hal tersebut dapat menyebabkan mata lelah serta nyeri kepala.</p>

        <h5>Diagnosis Presbiopi</h5>
        <p>Dalam menegakkan diagnosis presbiopi, dokter pertama-tama akan melakukan wawancara medis (anamnesis) dengan
            pasien untuk mengetahui keluhan dan riwayat kesehatan pasien secara menyeluruh. Setelah itu, dokter dapat
            melakukan pemeriksaan mata melalui uji refraksi untuk mengetahui apakah pasien menderita presbiopi, rabun
            jauh, rabun dekat, atau astigmatisme.</p>
        <p>Dokter juga dapat menggunakan obat tetes mata khusus untuk melebarkan pupil pasien guna memudahkan proses
            pemeriksaan mata. Obat tetes mata ini mungkin dapat membuat pasien menjadi lebih sensitif terhadap cahaya
            selama beberapa jam setelah pemeriksaan.</p>

        <h5>Cara Mengatasi Presbiopi</h5>
        <p>Tujuan utama pengobatan presbiopi adalah untuk membantu memfokuskan mata pada objek berjarak dekat. Adapun
            sejumlah metode yang dapat dilakukan untuk menangani presbiopi adalah sebagai berikut.</p>

        <h3>1. Penggunaan Kacamata atau Lensa Kontak</h3>
        <p>Untuk membantu penderita presbiopi agar dapat melihat objek jarak dekat dengan jelas, dokter dapat meresepkan
            kacamata khusus yang sesuai dengan kondisi mata pasien. Beberapa pilihan kacamata yang umum digunakan untuk
            menangani presbiopi adalah:</p>
        <ul>
            <li><strong>Kacamata baca:</strong> Kacamata yang hanya digunakan untuk membaca.</li>
            <li><strong>Lensa bifokal:</strong> Lensa dengan dua titik fokus untuk membantu melihat objek jarak jauh dan
                dekat.</li>
            <li><strong>Lensa trifokal:</strong> Lensa dengan tiga titik fokus untuk membantu melihat objek jarak jauh,
                menengah, dan dekat.</li>
            <li><strong>Lensa progresif:</strong> Lensa dengan garis pemisah titik fokus yang cenderung samar atau tidak
                terlihat.</li>
            <li><strong>Lensa office progressive:</strong> Lensa yang dirancang khusus untuk membantu seseorang dalam
                melakukan pekerjaan di kantor, seperti menulis atau menggunakan komputer.</li>
        </ul>
        <p>Selain kacamata, penderita presbiopi juga dapat menggunakan lensa kontak khusus untuk membantu memfokuskan
            penglihatan jarak dekat. Namun, lensa kontak tidak dianjurkan bagi seseorang yang memiliki gangguan pada
            kelopak mata, saluran air mata, atau menderita sindrom mata kering.</p>

        <h3>2. Penggunaan Obat Tetes Mata Khusus</h3>
        <p>Dokter juga dapat meresepkan obat tetes mata pilocarpine untuk mengecilkan pupil mata pasien sehingga dapat
            meningkatkan fokus mata saat melihat objek dengan jarak dekat. Namun, perlu diketahui bahwa obat tetes mata
            ini dapat menimbulkan sejumlah efek samping, seperti mata merah, nyeri kepala, dan gangguan penglihatan pada
            malam hari sehingga penggunaannya harus sesuai dan berada di bawah pengawasan dokter.</p>

        <h3>3. Bedah Refraktif</h3>
        <p>Bedah refraktif merupakan prosedur medis yang dilakukan dengan cara mengubah bentuk kornea untuk meningkatkan
            kemampuan penglihatan pasien. Beberapa metode bedah refraktif yang umum dilakukan untuk menangani presbiopi
            adalah:</p>
        <ul>
            <li><strong>Conductive keratoplasty:</strong> Prosedur yang menggunakan energi radiofrekuensi untuk
                memanaskan titik-titik di sekitar kornea sehingga dapat mengubah lengkungan kornea mata.</li>
            <li><strong>Laser-assisted in situ keratomileusis (LASIK):</strong> Prosedur yang dilakukan dengan memotong
                penutup tipis berengsel dari bagian depan mata, lalu menghilangkan lapisan dalam kornea menggunakan
                laser.</li>
            <li><strong>Laser-assisted subepithelial keratectomy (LASEK):</strong> Tindakan medis yang menggunakan laser
                untuk memperdalam lengkungan kornea dan membentuk kembali lapisan luar kornea.</li>
            <li><strong>Photorefractive keratectomy:</strong> Serupa dengan LASEK, prosedur ini bertujuan untuk
                membentuk kembali kornea menggunakan sinar laser. Namun, prosedur ini biasanya dilakukan dengan
                mengangkat seluruh lapisan luar kornea.</li>
            <li><strong>Small-incision lenticule extraction (SMILE) surgery:</strong> Prosedur minimal invasif dengan
                membuat jaringan berbentuk cakram di dalam kornea menggunakan laser dan mengangkatnya melalui sayatan
                kecil.</li>
        </ul>

        <h3>4. Implan Lensa</h3>
        <p>Implan lensa merupakan prosedur pembedahan dengan cara mengganti lensa mata pasien dengan lensa intraokular
            sintetis sehingga dapat memperbaiki penglihatan pasien. Namun, dalam beberapa kasus, prosedur ini dapat
            menurunkan fungsi mata dalam melihat objek dengan jarak dekat sehingga membuat pasien tetap memerlukan
            kacamata baca.</p>
        <p>Selain itu, implan lensa juga dapat menimbulkan efek samping lainnya, seperti penglihatan buram, mata silau,
            peradangan, infeksi, perdarahan, hingga glaukoma. Karena itu, alangkah baiknya untuk berkonsultasi dengan
            dokter terlebih dahulu sebelum menjalani prosedur pembedahan ini.</p>

        <h3>5. Inlay Kornea</h3>
        <p>Corneal inlay atau inlay kornea merupakan tindakan medis dengan cara memasukkan ring kecil berbahan plastik
            pada kornea mata pasien untuk mengubah lengkungan kornea. Ring tersebut berfungsi untuk memfokuskan cahaya
            pada kornea sehingga pasien dapat melihat objek dengan jarak dekat lebih jelas.</p>

        <h5>Pencegahan Presbiopi</h5>
        <p>Presbiopi adalah kondisi yang terjadi karena faktor penuaan sehingga cenderung sulit dicegah. Meski demikian,
            beberapa cara yang dapat dilakukan untuk meminimalkan risiko terjadinya presbiopi adalah sebagai berikut.
        </p>
        <ul>
            <li>Melakukan pemeriksaan mata secara berkala.</li>
            <li>Mengenakan kacamata dengan ukuran yang sesuai dengan kondisi mata.</li>
            <li>Menggunakan kacamata hitam untuk melindungi mata dari paparan sinar matahari.</li>
            <li>Mengonsumsi makanan yang baik untuk kesehatan mata, seperti wortel, sayuran hijau, telur,
                kacang-kacangan, dan lain sebagainya.</li>
            <li>Membaca dengan pencahayaan yang memadai.</li>
            <li>Mengistirahatkan mata secara berkala setelah menggunakan gadget.</li>
        </ul>

