        <h3 class="lead">Miopia</h3>

        <p>Miopi adalah gangguan penglihatan yang menyebabkan penderitanya tidak dapat melihat suatu objek dari
            jarak jauh. Kondisi ini termasuk dalam salah satu kelainan refraksi mata, di mana mata tidak dapat
            memfokuskan cahaya secara tepat di retina.</p>

        <p>Miopi dapat terjadi pada semua kalangan usia, baik anak-anak hingga orang dewasa. Ingin tahu penyebab
            dan cara menanganinya? Mari simak penjelasannya berikut ini.</p>

        <h4>Apa itu Miopi (Rabun Jauh)?</h4>
        <p>Miopi adalah masalah pada mata yang membuat penderitanya tidak dapat melihat suatu objek dari jarak
            jauh secara jelas. Kondisi ini sering dikenal oleh orang umum dengan sebutan mata minus. Tanda-tanda
            umum miopi adalah sakit kepala atau pusing, sulit membaca tulisan dari jauh, dan penglihatan buram.
        </p>

        <p>Miopi adalah kondisi yang biasanya dirasakan sejak anak-anak dan semakin memburuk seiring berjalannya
            waktu, bisa secara cepat atau perlahan. Miopi adalah gangguan mata yang sudah sangat umum terjadi
            pada semua kalangan usia, namun sering kali mulai muncul ketika umur 8-12 tahun.</p>

        <h4>Penyebab Miopi</h4>
        <p>Pada dasarnya, penyebab miopi adalah ketika cahaya yang masuk ke mata, jatuhnya tidak tepat pada
            retina. Kondisi tersebut terjadi karena bentuk bola mata pengidap miopi lebih panjang daripada bola
            mata normal.</p>

        <p>Selain itu, penyebab miopi juga bisa karena adanya kelainan pada kornea dan lensa mata yang berfungsi
            memfokuskan cahaya pada retina. Ketika kornea dan lensa mata tidak melengkung secara mulus dan
            merata, maka sinar cahaya tidak dapat dibiaskan dengan sempurna, sehingga terjadi kelainan refraksi.
        </p>

        <p>Ada beberapa faktor yang dapat meningkatkan risiko seseorang mengalami miopi, di antaranya yaitu:</p>
        <ul>
            <li>Terbiasa membaca atau menonton dengan jarak terlalu dekat</li>
            <li>Faktor genetik</li>
            <li>Kurang mendapatkan sinar matahari</li>
            <li>Kekurangan vitamin D</li>
        </ul>

        <h4>Jenis-jenis Miopi</h4>

        <p>Jenis-jenis miopi dibagi menjadi dua berdasarkan penyebabnya, yaitu miopi degeneratif dan tinggi.
            Berikut masing-masing penjelasannya.</p>

        <h5>Miopi Degeneratif</h5>
        <p>Miopi degeneratif adalah jenis gangguan rabun jauh yang cukup langka, sering kali muncul karena
            adanya faktor genetik. Pada kondisi ini, bola mata memanjang dengan cepat dan mengakibatkan miopi
            parah, sering kali terjadi pada remaja atau orang dewasa awal.</p>

        <p>Miopi degeneratif dapat bertambah parah seiring berjalannya waktu dan usia. Kondisi ini dapat membuat
            seseorang kesulitan melihat benda jarak jauh. Bahkan, berisiko mengalami komplikasi seperti
            glaukoma, retina lepas, dan pertumbuhan pembuluh darah yang tidak normal.</p>

        <h5>Miopi Tinggi</h5>
        <p>Miopi tinggi adalah kondisi rabun jauh yang lebih parah, terjadi ketika bola mata lebih panjang dari
            ukuran normal. Kondisi ini dapat membaik seiring berjalannya waktu, pada umur 20-30 tahun. Pengidap
            miopi tinggi memiliki risiko terkena gangguan mata seperti retina sobek dan glaukoma.</p>

        <h4>Gejala Miopi</h4>

        <p>Gejala rabun jauh atau miopi meliputi:</p>
        <ul>
            <li>Perlu menyipitkan mata atau menutup sebagian mata agar dapat melihat objek dengan jelas</li>
            <li>Penglihatan buram saat melihat benda dari jauh</li>
            <li>Mengalami sakit kepala karena mata kelelahan</li>
            <li>Kesulitan melihat jalan saat berkendara, terlebih pada malam hari</li>
        </ul>

        <p>Miopi adalah kondisi yang dapat terdeteksi sejak kecil. Beberapa gejala miopi pada anak-anak adalah:
        </p>
        <ul>
            <li>Tidak menyadari adanya benda jauh</li>
            <li>Sering menggosok mata</li>
            <li>Saat melihat televisi harus duduk lebih dekat dengan layar</li>
            <li>Sering mengedipkan mata</li>
            <li>Terus-menerus menyipitkan mata saat melihat sesuatu</li>
        </ul>

        <h4>Diagnosis Miopi</h4>

        <p>Miopi adalah kondisi yang dapat dipastikan atau didiagnosis melalui serangkaian pemeriksaan mata
            secara lengkap. Pemeriksaan mata dilakukan untuk mengetahui kemampuan penglihatan Anda serta
            menentukan jenis lensa yang tepat.</p>

        <p>Anda dapat melakukan tes mata di dokter spesialis ataupun optik. Salah satu tes yang paling umum
            dilakukan yaitu tes visus mata.</p>

        <h4>Cara Mengatasi Miopi</h4>

        <p>Cara mengatasi rabun jauh dapat dilakukan dengan beberapa upaya, berikut di antaranya:</p>

        <ol>
            <li><strong>Menggunakan Kacamata</strong><br>
                Penggunaan kacamata adalah salah satu cara mengatasi miopi tanpa operasi yang umum dilakukan
                pada pengidap miopi. Dengan bantuan kacamata, seseorang yang mengalami miopi dapat melihat
                dengan jelas, sekalipun dari jarak jauh. Penderita miopi menggunakan lensa cekung untuk membantu
                bayangan kembali terbentuk tepat pada retina.</li>

            <li><strong>Implantasi Lensa Buatan</strong><br>
                Upaya implantasi lensa buatan pada miopi adalah pencangkokan lensa pada mata dengan jenis lensa
                buatan yang disesuaikan kebutuhan pengidapnya. Selain memperbaiki penglihatan bagi pengidap
                miopi, cara ini juga dapat digunakan pada pengidap mata katarak.</li>

            <li><strong>Operasi LASIK</strong><br>
                Operasi LASIK (Laser-Assisted in Situ Keratomileusis) merupakan salah satu cara mengatasi miopi
                yang dapat mengembalikan penglihatan normal pengidapnya. Operasi ini dilakukan untuk memperbaiki
                bentuk kornea mata pada penderita miopi agar kembali normal dan dapat melihat jelas tanpa
                bantuan kacamata. Namun, tidak semua pengidap miopi dapat melakukan operasi LASIK.</li>
        </ol>

        <p>Pastikan berkonsultasi dengan dokter mata untuk mengetahui opsi pengobatan yang sesuai untuk kondisi
            miopi Anda.</p>

        <h4>Cara Mencegah Miopi</h4>

        <p>Miopi adalah kondisi yang dapat terjadi pada siapa saja dan kapan saja, terlebih jika memiliki
            keluarga dengan riwayat miopi. Untuk mencegahnya atau memperlambat perkembangannya, Anda dapat
            melakukan beberapa upaya sebagai berikut:</p>
        <ul>
            <li>Melakukan pemeriksaan mata secara teratur meski tidak merasakan gejala apa pun</li>
            <li>Menggunakan kacamata anti radiasi untuk melindungi mata dari sinar UV atau saat berada di depan
                gawai</li>
            <li>Mengonsumsi makanan yang baik untuk mata, seperti vitamin A dan omega-3</li>
            <li>Mengurangi aktivitas yang berisiko membuat mata kelelahan</li>
            <li>Perbanyak menghabiskan waktu di luar ruangan</li>
            <li>Mengontrol kondisi kesehatan, seperti diabetes dan hipertensi</li>
            <li>Menghindari kebiasaan merokok</li>
        </ul>

