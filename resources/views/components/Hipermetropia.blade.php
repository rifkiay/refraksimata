        <h3 class="lead">Hipermetropia</h3>

        <h4>Apa itu Hipermetropi (Rabun Dekat)?</h4>
        <p>Sebenarnya, apa itu hipermetropi? Hipermetropi adalah kondisi gangguan penglihatan yang membuat
            penderitanya kesulitan untuk melihat suatu benda dari dekat secara jelas.</p>
        <p>Hipermetropi adalah gangguan penglihatan yang juga biasa dikenal dengan istilah rabun dekat bagi orang
            awam.</p>
        <p>Karena itulah, penderita hipermetropi menggunakan lensa cembung atau positif untuk membantu
            penglihatannya.</p>
        <p>Jenis gangguan penglihatan satu ini sering terjadi pada orang dewasa. Namun, tidak menutup kemungkinan
            juga dapat terjadi pada orang muda atau bahkan anak-anak.</p>
        <p>Hipermetropi adalah kondisi yang berkebalikan dengan miopi. Lebih tepatnya, perbedaan miopi dan
            hipermetropi adalah dari jarak penglihatannya.</p>
        <p>Jika hipermetropi membuat pandangan jarak dekat penderitanya kabur, miopi merupakan gangguan optik dengan
            gejala seseorang kesulitan melihat benda dari jarak jauh.</p>
        <p>Lantaran kebalikan dari hipermetropi, miopi biasa dikenal dengan istilah rabun jauh.</p>

        <h4>Penyebab Hipermetropi</h4>
        <p>Penyebab hipermetropi adalah cahaya yang ditangkap oleh mata jatuh di bagian belakang retina.</p>
        <p>Di mana, retina dalam mata ini berfungsi untuk menerima gambaran dari cahaya yang didapatkan oleh mata
            lalu akan mengirimkan informasi tersebut ke otak.</p>
        <p>Hal tersebut biasanya diakibatkan oleh kondisi lensa atau kornea mata yang tidak normal.</p>
        <p>Di samping itu, ada beberapa faktor risiko dari hipermetropi, di antaranya:</p>
        <ul>
            <li>Faktor keturunan. Hal ini menjadi faktor risiko hipermetropi bagi kebanyakan anak-anak</li>
            <li>Berumur di atas 40 tahun</li>
            <li>Mengalami gangguan kesehatan tertentu, seperti diabetes, tumor atau kanker mata, dan lain sebagainya
            </li>
        </ul>

        <h4>Gejala Hipermetropi</h4>
        <p>Gejala rabun dekat atau hipermetropi cenderung tidak terasa oleh penderitanya. Walau demikian, mata rabun
            dekat memiliki ciri-ciri sebagai berikut:</p>
        <ul>
            <li>Benda-benda yang berjarak dekat dengan mata terlihat kabur</li>
            <li>Mata mudah terasa lelah</li>
            <li>Sering kali menyipitkan mata ketika sedang membaca atau melihat benda dari jarak dekat</li>
            <li>Sakit kepala setelah melihat benda dari jarak dekat dalam jangka waktu lama</li>
        </ul>

        <h4>Diagnosis Hipermetropi</h4>
        <p>Biasanya, langkah yang dilakukan oleh dokter dalam mendiagnosis hipermetropi adalah dengan melaksanakan
            tes ketajaman penglihatan.</p>
        <p>Tes ini dilakukan dengan meminta pasien untuk membaca huruf atau kata pada eye chart dengan ukuran yang
            bervariasi.</p>
        <p>Selain itu, dokter juga akan melakukan pemeriksaan pada bagian belakang mata.</p>
        <p>Langkah ini dokter lakukan dengan meneteskan suatu cairan khusus yang dapat melebarkan pupil.</p>
        <p>Setelah dipastikan mengalami hipermetropi, dokter akan memasangkan lensa tertentu dengan berbagai ukuran
            pada mata pasien untuk menentukan tingkat keparahan gangguan penglihatan tersebut.</p>
        <p>Dengan begitu, dokter dapat memilih tindakan penanganan yang sesuai untuk pasien.</p>

        <h4>Langkah Pengobatan Hipermetropi</h4>
        <p>Apabila sudah dipastikan menderita hipermetropi, dokter umumnya akan melakukan tindakan tertentu untuk
            mengatasi hal tersebut.</p>
        <p>Berikut beberapa langkah pengobatan yang biasa dilakukan oleh dokter dalam menangani pasien hipermetropi:
        </p>
        <h5>1. Lensa korektif</h5>
        <p>Lensa korektif merupakan langkah pengobatan sederhana yang dilakukan apabila tingkat keparahan
            hipermetropi cenderung ringan hingga sedang.</p>
        <p>Lensa korektif ini digunakan untuk membantu pasien dalam memfokuskan cahaya ke retina, sehingga benda
            tersebut bisa terlihat dengan jelas.</p>
        <p>Ada dua jenis lensa korektif untuk hipermetropi yang bisa pasien pilih, yaitu kacamata atau lensa kontak
            berjenis cembung.</p>

        <h5>2. Operasi Refraktif</h5>
        <p>Pengobatan untuk hipermetropi selanjutnya yaitu operasi refraktif. Operasi refraktif ini dilakukan dengan
            menggunakan laser untuk memperbaiki bentuk kornea mata.</p>
        <p>Tiga jenis operasi refraktif yang biasa dilakukan untuk menangani hipermetropi adalah sebagai berikut:
        </p>
        <ul>
            <li><strong>PRK atau Photorefractive keratectomy:</strong> prosedur operasi dengan mengangkat lapisan
                luar kornea untuk memperbaiki bentuk kornea tersebut. Operasi ini biasanya tidak dianjurkan untuk
                pasien yang menderita penyakit tertentu, seperti diabetes, katarak, glaukoma, dan lain sebagainya.
            </li>
            <li><strong>LASIK atau laser-assisted in situ keratomileusis:</strong> mengikis sebagian lapisan kornea
                dan membuat flap atau lapisan untuk memperbaiki bentuknya.</li>
            <li><strong>LASEK atau laser-assisted subepithelial keratectomy:</strong> membuat flap atau lipatan pada
                kornea dengan alat pemotong khusus (microkeratome).</li>
        </ul>

        <p>Demikian ulasan mengenai hipermetropi yang perlu Anda ketahui.</p>
        <p>Jadi, hipermetropi adalah masalah penglihatan yang dapat mengganggu aktivitas sehari-hari sehingga Anda
            perlu melakukan beberapa perawatan melalui tindakan medis.</p>

